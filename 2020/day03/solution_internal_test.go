package day03

import (
	"strings"
	"testing"

	"github.com/stretchr/testify/assert"
)

const testCase = `..##.......
#...#...#..
.#....#..#.
..#.#...#.#
.#...##..#.
..#.##.....
.#.#.#....#
.#........#
#.##...#...
#...##....#
.#..#...#.#`

func TestParse(t *testing.T) {
	expected := [][]string{
		[]string{".", ".", "#", "#", ".", ".", ".", ".", ".", ".", "."},
		[]string{"#", ".", ".", ".", "#", ".", ".", ".", "#", ".", "."},
		[]string{".", "#", ".", ".", ".", ".", "#", ".", ".", "#", "."},
		[]string{".", ".", "#", ".", "#", ".", ".", ".", "#", ".", "#"},
		[]string{".", "#", ".", ".", ".", "#", "#", ".", ".", "#", "."},
		[]string{".", ".", "#", ".", "#", "#", ".", ".", ".", ".", "."},
		[]string{".", "#", ".", "#", ".", "#", ".", ".", ".", ".", "#"},
		[]string{".", "#", ".", ".", ".", ".", ".", ".", ".", ".", "#"},
		[]string{"#", ".", "#", "#", ".", ".", ".", "#", ".", ".", "."},
		[]string{"#", ".", ".", ".", "#", "#", ".", ".", ".", ".", "#"},
		[]string{".", "#", ".", ".", "#", ".", ".", ".", "#", ".", "#"},
	}
	actual := parse(strings.NewReader(testCase))
	assert.Equal(t, expected, actual, "they should be equal")
}
